from langchain.prompts import PromptTemplate

def get_resume_guideline_prompt_template():
    """
    이력서 작성 지침을 제공하는 프롬프트.
    """
    template = """
    당신은 이력서를 작성하려고 합니다. 다음의 이력서 구성 요소에 따라 상세한 정보를 채워 넣어야 합니다. 없는 정보는 NONE으로 반환합니다.

    # 이력서 작성 지침

    ## 기본 정보
    - Info: 사용자의 기본 정보 (이름, 연락처 등).
    - Life Style: 사용자와 관련된 생활 방식, 성향 등.
    - Tools & Skills: 사용자에게 익숙한 도구와 기술들 (예: 프로그래밍 언어, 툴, 프레임워크).

    ## Timeline 및 정보
    - Timeline을 작성하되 중간에 공백이 없도록, 모든 경력과 프로젝트를 포함해 주세요.

    ## 프로젝트 설명
    - **역할**: 해당 프로젝트에서 맡은 역할을 설명하세요.
    - **인력 구성**: 팀원 수와 팀 내 역할 구성을 설명하세요.
    - **주요 업무**: 프로젝트에서 맡았던 핵심 업무를 기술하세요.
    - **사용 언어 및 개발 환경**: 사용한 프로그래밍 언어와 개발 도구를 명시하고, 사용 이유도 포함하세요.
    - **시스템 아키텍처**: 프로젝트 시스템의 구조를 완벽히 이해하고 있음을 나타내세요.

    ## 시행착오 및 개선점
    - **시도**: 프로젝트에서 시도한 것들을 설명하세요.
    - **한계점**: 프로젝트에서 발생한 문제나 제한사항을 기술하세요.
    - **인사이트**: 문제를 해결하며 얻은 통찰력을 설명하세요.

    ## 면접 TIP
    - **면접관도 긴장함**: 면접관은 지원자의 기술 수준을 파악하려 하므로, 이력서 중간중간에 질문할 거리를 많이 남겨두세요.
    - **기술 강조**: 강조하고 싶은 기술을 **구체적 용어**를 사용해 작성하세요. 이를 통해 면접관이 쉽게 질문할 수 있게끔 유도하세요.
    - **철저한 이해**: 질문에 대답할 수 있는 깊은 이해도를 가지도록 준비하세요.

    이 지침을 바탕으로 이력서를 작성하세요.
    """

    return PromptTemplate(template=template, input_variables=["name", "contact", "skills", "projects", "insights"])
